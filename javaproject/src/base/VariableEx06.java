package base;

public class VariableEx06 {

	public static void main(String[] args) {
		String name = "홍길동";
		int age = 25;
		System.out.println("내 이름은 " + name + "이고 나이는 " + age + "세입니다.");
		System.out.printf("내 이름은 %s이고 나이는 %d세입니다.", name, age);						//printf 에서 f 는 format(형식).. %d에서 d는 decimal(10진수)
		
	}
}
